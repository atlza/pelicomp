            <h1>A propos de Péloches.</h1>
            <h2>Origine</h2>
            <p>
                L'idée de <strong>Péloches</strong> vient du <a href="https://discord.gg/m5JAWqUGmx">Discord "Les Argenteux"</a> sur lequel un bot avait été développé qui allait checker les tarifs des pellicules entre autres.
                Lorsque celui-ci cessa de fonctionner, il fut porté sur Google Doc par l'un des utilisateurs du Discord.<br/>
                Là le travail devient titanesque devant le nombre de pellicules existantes à mettre à jour quotidiennement ou presque pour chaque boutique.
                Sans parler du soucis qu'il soit sur un outil propriété de Google.<br/>
                L'idée émerge donc de l'automatiser et de le porter sur un site indépendant, porté par un <strong>logiciel libre</strong>.
            </p>
            <h2>Contribuer</h2>
            <p>
                L'idée à terme est d'ouvrir les contributions à tous pour que l'offre présentée soit la plus large possible.
                Pour l'instant, cependant, celle-ci est soumise à validation.<br />
                Si vous souhaitez contribuer en ajout des pellicules, des boutiques ou des offres, cliquez sur "Contribuer",
                saisissez votre email puis rendez-vous sur le <a href="https://discord.gg/m5JAWqUGmx">"Discord les Argenteux"</a>, interpellez <strong>LeZla</strong> sur le chan <strong>"FilmBot"</strong> qui vous ouvrira les droits.<br>
                Le temps de mettre en place les mécanismes de contrôle nécessaire et les contributions seront ouvertes à tous.
            </p>
            <h3>Bugs, données manquantes ou erronées</h3>
            <p>
                Toute demande de correction ou d'évolution technique doit être faite sur <a href="https://github.com/atlza/pelicomp">Github</a> via la création d'un ticket.<br/>
                Toute question ou remarque sur les données doit être faite sur <a href="https://discord.gg/m5JAWqUGmx">Discord</a> dans le chan FilmBot.
            </p>
            <h2>Logiciel</h2>
                <h3>Logiciel libre</h3>
                <p>
                    Le code source du site internet derrière "Peloches" est disponible gratuitement sous-licence aGplv3 sur <a href="https://github.com/atlza/pelicomp">github</a>,
                    vous êtes libre de le copier et de le modifier.<br />
                    N'hésitez pas lancer votre propre comparateur de prix sur le sujet qui vous intéresse et à "pinger" l'auteur du code sur Mastodon <a href="https://mamot.fr/@atlza">@atlza</a>.<br />
                </p>
                <h3>Cookies</h3>
                <p>
                    Ce site ne montre pas de bandeau cookies.<br/>
                    Tout simplement car il ne dépose aucun cookies non techniques (essentiels au fonctionnement du site) ni ne partage vos données avec aucun partenaires.
                </p>
